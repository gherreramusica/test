<h1>Política Editorial</h1>
<p>
<strong>¿Quiénes Somos?</strong> 

Oromartv es un medio privado con fines de lucro. Creado bajo las leyes y normas del estado ecuatoriano; supervisado y regulado por todos los entes de control del estado ecuatoriano.

<strong>Financiamiento</strong> 

Su financiamiento proviene principalmente de la inversión privada, pero no limitada al sector público, quienes mediante la compra de espacios publicitarios difunden un mensaje comunicacional a nuestra audiencia sin que esta condicione los principios y valores de la empresa.

<strong>Antecedentes</strong>

Los medios de comunicación son constructores activos del tejido social. A través del relato de los hechos y acontecimientos aportamos a la formación del criterio y del pensamiento colectivo indispensable para la toma de decisiones y la cimentación de nuestra identidad. En Oromartv consideramos importante que los medios de comunicación en su diversidad de pensamientos e ideas sirvan como fuentes contrastables de hechos y acontecimientos para que la ciudadanía con base a un amplio espectro informativo pueda construir su propia matriz de principios que le permitan desarrollarse y aportar al desarrollo de un país mejor de acuerdo a sus propios valores y principios. 

 Este argumento conlleva un alto grado de responsabilidad y planificación en el ejercicio diario de la comunicación y requiere la construcción y el establecimiento de una línea de principios y valores a la que todos los profesionales que estén inmersos en los procesos de construcción de un mensaje en Oromartv, recurran como guía para comprobar el resultado de su trabajo y garantice un trato adecuado a la información.

<strong>Filosofía</strong>

Como punto de partida, el enfoque comunicacional sobre los productos ofertados en la programación de Oromartv pueden encontrar raíz en un amplio espectro de temas que son planteados desde la agenda informativa y por quienes los realizan siempre y cuando estos no transgredan el derecho esencial a la vida, la propiedad y la libertad. 

En Oromartv no nos consideramos dueños de la verdad absoluta, sino únicamente de nuestra propuesta informativa. No nos consideramos la única voz, sino una voz que desde su propia realidad busca aportar su visión y perspectiva para la construcción de un país mejor. 

En este medio los periodistas tienen una voz fuerte producto de su trabajo de investigación y especialización. 

No solo buscan contar los hechos sino dar a conocer el impacto de tales acontecimientos y cómo afectan a los valores de la vida, libertad y propiedad de sus ciudadanos.

¿Quién es nuestro público?

Son ecuatorianos y extranjeros, mayores de edad con intereses en el ámbito político, económico, social y cultural del Ecuador. Son un público de pensamiento liberal interesados en el desarrollo humano integral. Buscan de forma activa construir soluciones que resuelvan distintas necesidades tanto en el Ecuador como en el mundo para sostener a sus familias y así aportar al desarrollo individual y colectivo. Son actores activos del desarrollo productivo de su país. Son emprendedores, trabajadores, estudiantes y profesionales. Son defensores de la paz, del orden y de la democracia; de la vida, libertad y del progreso. 

Comité editorial

Esta conformado por las personas a cargo de la propuesta informativa del contenido de Oromartv. El comité editorial supervisa el contenido así como todos los elementos de cada uno de los espacios de entretenimiento e información de Oromartv. Son los responsables de la línea editorial. Los miembros del comité editorial conocen a profundidad la línea editorial del medio de comunicación y están capacitados para normar y regular los productos informativos de cada una de las emisiones.


